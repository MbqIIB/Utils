package com.fisglobal.xpress.emea.mblox;


public class NotificationStatus {
    private MbloxConnectionStatus mbloxConnectionStatus;
    private NotificationDetails notificationDetails;
    private NotificationRequestResultDetails notificationRequestResultDetails;

    public NotificationStatus() {
    }

    public NotificationStatus(MbloxConnectionStatus mbloxConnectionStatus,
                              NotificationDetails notificationDetails,
                              NotificationRequestResultDetails notificationRequestResultDetails) {

        this.mbloxConnectionStatus = mbloxConnectionStatus;
        this.notificationDetails = notificationDetails;
        this.notificationRequestResultDetails = notificationRequestResultDetails;
    }

    public MbloxConnectionStatus getMbloxConnectionStatus() {
        return mbloxConnectionStatus;
    }

    public void setMbloxConnectionStatus(MbloxConnectionStatus mbloxConnectionStatus) {
        this.mbloxConnectionStatus = mbloxConnectionStatus;
    }

    public NotificationDetails getNotificationDetails() {
        return notificationDetails;
    }

    public void setNotificationDetails(NotificationDetails notificationDetails) {
        this.notificationDetails = notificationDetails;
    }

    public NotificationRequestResultDetails getNotificationRequestResultDetails() {
        return notificationRequestResultDetails;
    }

    public void setNotificationRequestResultDetails(
            NotificationRequestResultDetails notificationRequestResultDetails) {
        this.notificationRequestResultDetails = notificationRequestResultDetails;
    }
}
